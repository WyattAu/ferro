/// Binary search lookup by key.
pub fn get(key: &str) -> Option<&'static str> {
    EN.binary_search_by(|(k, _)| (*k).cmp(key))
        .ok()
        .map(|i| EN[i].1)
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_all_keys_unique() {
        let mut seen = std::collections::HashSet::new();
        for (key, _) in EN {
            assert!(seen.insert(*key), "duplicate key: {key}");
        }
    }

    #[test]
    fn test_sorted() {
        for window in EN.windows(2) {
            assert!(
                window[0].0 < window[1].0,
                "key '{}' should come after '{}' (not sorted)",
                window[1].0,
                window[0].0,
            );
        }
    }

    #[test]
    fn test_known_lookup() {
        assert_eq!(get("common.cancel"), Some("Cancel"));
        assert_eq!(get("dialog.delete_confirm.title"), Some("Confirm Delete"));
    }

    #[test]
    fn test_missing_key_returns_none() {
        assert_eq!(get("nonexistent.key.abc"), None);
    }

    #[test]
    fn test_translate_fallback() {
        assert_eq!(
            crate::i18n::translate(crate::i18n::Locale::En, "no.such.key"),
            "no.such.key"
        );
    }

    #[test]
    fn test_locale_tags() {
        assert_eq!(crate::i18n::Locale::En.tag(), "en");
        assert_eq!(crate::i18n::Locale::default(), crate::i18n::Locale::En);
    }
}
